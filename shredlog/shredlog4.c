int init_fd (int fd_count, const int fd_map[ ])
{
        int i = 0;
        int j = 0;
        int nTempFd = 0;
        int fd_cpy[fd_count];

        memcpy (fd_cpy, fd_map, fd_count * sizeof (int));
        while (i < fd_count)
        {
                if (i != fd_cpy[i]) {
                        nTempFd = dup(i);
                        if (nTempFd != EBADF) {
                                for (j = i+1; j < fd_count; j++) {
                                        if (fd_cpy[j] == i) {
                                                fd_cpy[j] = nTempFd;
                                        }
                                }
                        }

                        if (dup2(fd_map[i], i) < 0) {
                                perror("_spawn:dup2");
                                exit (-1);
                        }
                }
                i++;
        }
        return -1;
}

pid_t spawn( const char * path,
        int fd_count,
        const int fd_map[ ],
        char * const argv[ ]) {

        pid_t   nPid = -1;

        if (access (path, R_OK | X_OK) != 0)
        {
                perror ("access");
                return (-1);
        }

        switch ((nPid = fork ()))
        {
                case 0:         /* child */
                        init_fd (fd_count, fd_map);
                        execv (path, argv);
                        //TracePar (XL_TRACE_ERROR, "[ER] exec fail", 0, errno);
                        exit (0);

                case -1:        /* error */
                        //TracePar (XL_TRACE_ERROR, "[ER] fork fail", 0, errno);
                        return (-1);

                default:        /* parent */
                        return (nPid);
        }

        return -1;
}
