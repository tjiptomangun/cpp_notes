static int Dec2Ascii (char *pDest, char c)
{
  unsigned char c1;
  unsigned char c2;

  c1 = (unsigned char) c >> 4;
  c2 = c & 0x0F;
  if ((c1 > 9)  ||  (c2 > 9))
    {
      return (-1);
    }

  pDest[0] = c1 + '0';
  pDest[1] = c2 + '0';
  return (0);

} /* End of Dec2Ascii */



static char *StrDec2Ascii (char *pDest, char *pSrc, int nSrcLen)
{
  int  m, n;

  for (m = 0, n = 0; n < nSrcLen; ++n, m += 2)
    {
      if (Dec2Ascii (&pDest[m], pSrc[n]) == -1)
        return (pDest);
    }
  pDest[m] = 0;

  return (pDest);
} /* End of StrDec2Ascii */
